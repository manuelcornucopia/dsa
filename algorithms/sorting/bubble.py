def bubble_sort(arr):
    n = len(arr)
    for i in range(n):
        swapped = False
        # After each pass, the last i elements are already in place.
        for j in range(0, n - i - 1):
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]  # Swap.
                swapped = True
        if not swapped:  # No swaps -> list is sorted.
            break
    return arr


